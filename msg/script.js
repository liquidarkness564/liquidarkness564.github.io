function DeleteDuplicates(arr) {
    let array = arr.slice();
    let duplicate = false;
    for (let i = 0; i < array.length; i++) {
        for (let j = 0; j < array.length; j++) {
            if (array[i].name === array[j].name & duplicate) {
                array.splice(i, 1);
            }
            if (array[i].name === array[j].name) {
                duplicate = true;
            }
        }
        duplicate = false;
    }
    return array;
}

